new_pids_39 = [int(m) for m in re.findall(r'^\((\d+),', BLOCK_39_40, re.M)]
all_new = new_pids_37 + new_pids_39
dup = [p for p in all_new if p in existing]
if dup:
    print(f"❌ 重复 PID: {dup}")
    exit(1)

# 找 JS_HEADER 插入点
m = re.search(r"\nJS_HEADER\s*=\s*'''", content)
if not m:
    print("❌ 找不到 JS_HEADER 标记")
    exit(1)

insert_pos = m.start()
new_content = (
    content[:insert_pos]
    + '\n' + BLOCK_37_38
    + '\n' + BLOCK_39_40
    + '\n'
    + content[insert_pos:]
)

open(BP, 'w', encoding='utf-8').write(new_content)
injected = len(all_new)
print(f"✅ 注入成功，Lesson 37-40 PID 共 {injected} 个: {all_new}")
print(f"   文件总行数: {len(new_content.splitlines())} 行")
